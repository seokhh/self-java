/* 자바에서 /는 나누기 연산자이다.
   x/y 는 x를 y로 나누는 것인데 빈칸에 들어갈 타입은 무엇이면 출력되는 결과와 이유 설명*/
public class Main {
    public static void main(String[] args){
        int x = 5;
        int y = 2;
        int result = x / y; /* result 앞이 빈칸으로 되어있었음 답은 2이고 인트타입으로 계산결과가 나와야 함*/
        System.out.println(result);
        }
    }

